variable_x = 10  # Initial declaration of variable_x with an int value
print(variable_x)

variable_x = 5.0  # Assigning a float value to variable_x
print(variable_x)

variable_x = True  # Assigning a boolean value to variable_x
print(variable_x)

variable_x = "I'm a string now"  # Assigning a string value to variable_x
print(variable_x)

variable_x = 0  # Assigning another value to variable_x
print(variable_x)
